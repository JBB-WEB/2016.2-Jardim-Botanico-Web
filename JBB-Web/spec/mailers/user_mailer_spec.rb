require 'rails_helper'

feature "Sign up" do
  let(:user) { build(:user) }
  scenario 'User is not registered' do
    reset_mailer
    sign_up(user.email, user.password)
    expect(page).to have_text('A message with a sign_in link has been sent to your email address.
                               Please open the link to activate your account')
    expect(unread_emails_for(user.email).count).to eq(1)
    open_email(user.email)
    expect(current_email).to have_body_text('You can sign_in your account email through the link below')
    click_email_link_matching(/#{sign_in_path}/)
    expect(page).to have_content('Sign in')
    sign_in(user)
    expect(page).to have_content('Sign out')
  end
end
